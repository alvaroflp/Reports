SELECT SEGMENT_NAME AS "TABELABD",
       CASE
           WHEN BYTES / POWER(1024, 1) < 1024 THEN ROUND(BYTES / POWER(1024, 1), 2) || ' Kb'
           WHEN BYTES / POWER(1024, 2) < 1024 THEN ROUND(BYTES / POWER(1024, 2), 2) || ' Mb'
           WHEN BYTES / POWER(1024, 3) < 1024 THEN ROUND(BYTES / POWER(1024, 3), 2) || ' Gb'
           WHEN BYTES / POWER(1024, 4) < 1024 THEN ROUND(BYTES / POWER(1024, 4), 2) || ' Tb'
           ELSE ROUND(BYTES / POWER(1024, 5), 2) || ' Pb'
       END AS "TAMANHO"
FROM USER_SEGMENTS
ORDER BY BYTES DESC;
